"""Desafio 001.1
Crie um programa que escreva 'Olá, Mundo!' na tela usando emoticons"""

import emoji
print(emoji.emojize('Olá :earth_americas:!', use_aliases=True))
print(emoji.emojize('Olá :earth_africa:!', use_aliases=True))
print(emoji.emojize('Olá :earth_asia:!', use_aliases=True))
print(emoji.emojize('Olá Mundo! :sunglasses:', use_aliases=True))
